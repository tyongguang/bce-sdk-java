package com.baidubce.services.bos.model;

import java.util.List;

/**
 * Contains the data returned by Baidu Bos from the <code>ProcessObject</code>
 * operation. Use this request to access information about the new object
 * created from the <code>ProcessObject</code> request
 */
public class ProcessObjectResponse extends BosResponse {
    private Integer code;

    private String requestId;

    private List<String> jobIds = null;

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getRequestId() {
        return requestId;
    }

    public void setRequestId(String requestId) {
        this.requestId = requestId;
    }

    public List<String> getJobIds() {
        return jobIds;
    }

    public void setJobIds(List<String> jobIds) {
        this.jobIds = jobIds;
    }

}